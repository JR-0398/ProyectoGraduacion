    private void obtenerpregunta(){
        cConexion con = new cConexion();
        //usNombre, usApellido, usPregunta,
        String usDui = txtDui.getText();
        String usPregunta, usRespuesta, usId;
        String sql = "SELECT id_usuario, usPregunta, usRespuesta FROM usuario WHERE usDui = '"+usDui+"'";
        PreparedStatement ps;
        ResultSet res;
        try {
            ps = con.conexion().prepareStatement(sql);
            res = ps.executeQuery();
            if(res.next()){
                usPregunta = res.getString("usPregunta");
                txtPregunta.setText(usPregunta);
                usRespuesta = res.getString("usRespuesta");
                lblRespuestaDB.setText(usRespuesta);
                usId = Integer.toString(res.getInt("id_usuario"));
                lblId.setText(usId);
            }else{
                JOptionPane.showMessageDialog(null, "Ha ocurrido un error", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error: "+e.toString(), "Error", JOptionPane.ERROR_MESSAGE);
        }  
    }
    
