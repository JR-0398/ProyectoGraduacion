    private void btnDuiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDuiActionPerformed
        // TODO add your handling code here:
        String usDui = txtDui.getText();
        if(usDui.equals("")){
            JOptionPane.showMessageDialog(null, "Actualmente el DUI esta vacio, Ingrese su DUI para poder obtener continuar con el proceso de cambiar su contraseña ", "DUI vacio", JOptionPane.INFORMATION_MESSAGE);       
        }else if(usDui.length() <= 8){
            JOptionPane.showMessageDialog(null, "El numero de DUI debe tener un total de 9 caracteres", "Dui incompleto", JOptionPane.INFORMATION_MESSAGE);
        }else{
            ResponderPregunta();
            obtenerpregunta();
        } 
    }//GEN-LAST:event_btnDuiActionPerformed

    private void btnRespuestaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRespuestaActionPerformed
        // TODO add your handling code here:
        String usResp = txtRespuesta.getText();
        String dbResp = lblRespuestaDB.getText();
        String usId = lblId.getText();
        if(usResp.equals("")){
            JOptionPane.showMessageDialog(null, "Verifique su respuesta, el campo actualemnte se encuentra vacio", "Respuesta vacio", JOptionPane.INFORMATION_MESSAGE);
        }else if (dbResp.equals(usResp)){
            int resp = JOptionPane.showConfirmDialog(null, "¿Confirme si desea cambiar su contraseña actual por una nueva contraseña?", "Advertencia", JOptionPane.YES_NO_OPTION);
            if (resp==JOptionPane.YES_OPTION){
                CambiarPass newForm = new CambiarPass();
                newForm.obtenerDato(usId);
                newForm.setVisible(true);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Se ha cancelado el cambio de contraseña", "Cambio de contraseña cancelado", JOptionPane.INFORMATION_MESSAGE);
                NuevoDui();
            }
        }else{
            JOptionPane.showMessageDialog(null, "La actual respuesta no es igual, compruebe que la esta escribiendo de manera correcta", "Respuesta incorrecta", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRespuestaActionPerformed

